// WHILE

let index = 0
while (index <= 10){
    // console.log(`Value of index is ${index}`);
    index = index + 1
}

let myArray = ["Ayush", "Ritik", "Raj", "Ritu"]
let arr = 0
while(arr < myArray.length){
    // console.log(`Value is ${myArray[arr]}`);
    arr = arr + 1
}

